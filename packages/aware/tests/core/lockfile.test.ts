import { describe, it, expect, beforeEach } from "vitest";
import * as fs from "node:fs/promises";
import * as os from "node:os";
import * as path from "node:path";
import { readLockfile } from "../../src/core/lockfile.js";

describe("readLockfile — pnpm v9", () => {
  let tmp: string;
  beforeEach(async () => {
    tmp = await fs.mkdtemp(path.join(os.tmpdir(), "aware-lockfile-pnpm-"));
  });

  it("reads resolved versions from importers.*", async () => {
    await fs.writeFile(
      path.join(tmp, "pnpm-lock.yaml"),
      `lockfileVersion: '9.0'
importers:
  .:
    dependencies:
      next:
        specifier: ^15.1.0
        version: 15.1.2
      react:
        specifier: ^19.0.0
        version: 19.0.0
    devDependencies:
      typescript:
        specifier: ^5.5.0
        version: 5.5.4
`,
    );
    const map = await readLockfile(tmp);
    expect(map.get("next")).toBe("15.1.2");
    expect(map.get("react")).toBe("19.0.0");
    expect(map.get("typescript")).toBe("5.5.4");
  });

  it("strips pnpm's peer-version suffix from resolved versions", async () => {
    await fs.writeFile(
      path.join(tmp, "pnpm-lock.yaml"),
      `lockfileVersion: '9.0'
importers:
  .:
    devDependencies:
      tsup:
        specifier: ^8.5.1
        version: 8.5.1(postcss@8.5.9)(typescript@6.0.2)
`,
    );
    const map = await readLockfile(tmp);
    expect(map.get("tsup")).toBe("8.5.1");
  });
});

describe("readLockfile — npm v3", () => {
  let tmp: string;
  beforeEach(async () => {
    tmp = await fs.mkdtemp(path.join(os.tmpdir(), "aware-lockfile-npm-"));
  });

  it("reads top-level dependencies from packages tree", async () => {
    await fs.writeFile(
      path.join(tmp, "package-lock.json"),
      JSON.stringify({
        lockfileVersion: 3,
        packages: {
          "": { name: "root", version: "0.0.0" },
          "node_modules/next": { version: "14.2.10" },
          "node_modules/react": { version: "18.3.1" },
          "node_modules/react/node_modules/nested": { version: "9.9.9" },
        },
      }),
    );
    const map = await readLockfile(tmp);
    expect(map.get("next")).toBe("14.2.10");
    expect(map.get("react")).toBe("18.3.1");
    // Nested duplicate — must NOT shadow top-level.
    expect(map.get("nested")).toBeUndefined();
  });
});

