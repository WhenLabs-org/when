describe("readLockfile — yarn berry", () => {
  let tmp: string;
  beforeEach(async () => {
    tmp = await fs.mkdtemp(path.join(os.tmpdir(), "aware-lockfile-berry-"));
  });

  it("parses berry's `foo@npm:range` headers and ignores __metadata", async () => {
    await fs.writeFile(
      path.join(tmp, "yarn.lock"),
      `# This file is generated by running "yarn install" inside your project.

__metadata:
  version: 8
  cacheKey: 10

"next@npm:^15.1.0":
  version: 15.1.2
  resolution: "next@npm:15.1.2"

"react@npm:^19.0.0":
  version: 19.0.0
  resolution: "react@npm:19.0.0"
`,
    );
    const map = await readLockfile(tmp);
    expect(map.get("next")).toBe("15.1.2");
    expect(map.get("react")).toBe("19.0.0");
    // __metadata must not be captured as a package name.
    expect(map.has("__metadata")).toBe(false);
  });

  it("handles scoped packages in yarn headers", async () => {
    await fs.writeFile(
      path.join(tmp, "yarn.lock"),
      `"@types/node@^20":
  version "20.11.0"
`,
    );
    const map = await readLockfile(tmp);
    expect(map.get("@types/node")).toBe("20.11.0");
  });
});

describe("readLockfile — priority when multiple lockfiles exist", () => {
  let tmp: string;
  beforeEach(async () => {
    tmp = await fs.mkdtemp(path.join(os.tmpdir(), "aware-lockfile-multi-"));
  });

  it("prefers pnpm-lock.yaml over package-lock.json and yarn.lock", async () => {
    await fs.writeFile(
      path.join(tmp, "pnpm-lock.yaml"),
      `lockfileVersion: '9.0'
importers:
  .:
    dependencies:
      next:
        specifier: ^15.1.0
        version: 15.2.0
`,
    );
    await fs.writeFile(
      path.join(tmp, "package-lock.json"),
      JSON.stringify({
        lockfileVersion: 3,
        packages: { "node_modules/next": { version: "14.0.0" } },
      }),
    );
    await fs.writeFile(
      path.join(tmp, "yarn.lock"),
      `"next@^15.0.0":\n  version "13.0.0"\n`,
    );
    const map = await readLockfile(tmp);
    // pnpm wins.
    expect(map.get("next")).toBe("15.2.0");
  });
});

describe("readLockfile — fallback", () => {
  let tmp: string;
  beforeEach(async () => {
    tmp = await fs.mkdtemp(path.join(os.tmpdir(), "aware-lockfile-none-"));
  });

  it("returns empty map when no lockfile present", async () => {
    const map = await readLockfile(tmp);
    expect(map.size).toBe(0);
  });

  it("returns empty map when lockfile is unparseable", async () => {
    await fs.writeFile(path.join(tmp, "pnpm-lock.yaml"), "nope: :: :: nonsense");
    const map = await readLockfile(tmp);
    // Either empty from parse error, or empty from missing expected shape.
    expect(map.size).toBe(0);
  });
});
